class DoubleTypeExample{
	public static void main(String args[])
	{
		double price;
		double fees;
		double distance;
		double calories;
		double area;
		
		price = 987.654;
		fees = 46.90;
		distance = 45.89;
		calories = 203.67;
		area = 5.98;
		
		
		System.out.println("price of a product is "+price);
		System.out.println("fees of a student is "+fees);
		System.out.println("distance is "+distance+"km");
		System.out.println("calories in a cup of rice "+calories);
		System.out.println("area of circle is"+area);
	}
}